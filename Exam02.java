
public class Exam02 {

	public static void main(String[] args) {
		// ctrl D : 한줄 삭제
		// 여러가지 변수의 특징
		int num = 20;            // 정수형 변수 num을 선언하고 20을 대입(할당)했다.
		// 1. 같은 이름의 변수를 생성할 수 없다
		// int num = 30;   -> 위에 num이 선언되어 같은 이름 안됨.
		num = 50;
		// 2. 변수에 값을 새로 담는 순간 이전의 값은 사라지게 된다.
		System.out.println(num); // -> 50이 출력됨
		// int age = 20;
		int age;
		age = 20;
		// 3. 따로 쓸 수 있음
		System.out.println(age);
		
		
		
		
	}

}
